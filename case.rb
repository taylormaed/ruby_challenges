weather = 'rainy'
case weather
when 'cloudy'
  puts "Take your umbrella."
when 'rainy'
  puts "Wear your cutest rainboots!"
when 'windy'
  puts "Pull out a scarf and hat."
when 'snowy'
  puts "Dig out your gloves and boots."
when 'sunny'
  puts "Wear your bathing suit!"
else
  puts "Seek shelter!"
end
